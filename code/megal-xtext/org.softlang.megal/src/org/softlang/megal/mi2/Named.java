package org.softlang.megal.mi2;

/**
 * <p>
 * Base class for named model elements.
 * </p>
 * 
 * @author Pazuzu
 *
 */
public abstract class Named extends Element {
	/**
	 * <p>
	 * Getter for the name
	 * </p>
	 * 
	 * @return Returns the name of the element
	 */
	public abstract String getName();
}